Operators
------------------
Applications use operators to process the data entered by the user.

An operator is a set of one or more characters which is used for calculation or comparisons.

Operators can transform one or more data values which are known as operands into a new value.

x + y 
x,y are operands
+ is an operator


Operators in c#
-------------------------
Arithmetic operators(+,-,*,/,%)
Arithmetic Assignment operators (+=,-=,*=,/=,%=)
Unary operators (++,--)
Comparison operators (>,<,>=,<=,==,!=)
Logical operators (&&(and), || (or), ! (not), ^ (carrot or cap) )

^ - It returns true if either expression1 or expression2 is true.
^ - It returns false if both expression1 and expression2 are true or if both expression1 and expression2 are false.






